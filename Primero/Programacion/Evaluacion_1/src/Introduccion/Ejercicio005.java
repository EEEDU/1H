package Introduccion;
/*
 * Decir si un número es par o impar
 */

public class Ejercicio005 {

	public static void main(String[] arg) {
		int x;
		
		x = 40;
		
		if(x % 2 == 0) {
			System.out.println("El número es par");
		}
		else {
			System.out.println("El número es impar");
		}
	}
}
